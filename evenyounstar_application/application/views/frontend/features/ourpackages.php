function mathCalculation() {
    var result = document.getElementById('id_phone').value;
	var operand = document.getElementById('operand').value;
	var num1 = document.getElementById('num1').value;
	var num2 = document.getElementById('num2').value;
	if(operand==0){
		var calculate = parseInt(num1) + parseInt(num2);
	}
	else if(operand==1){
		var calculate = parseInt(num1) - parseInt(num2);
	}
	else if(operand==2){
		var calculate = parseInt(num1) * parseInt(num2);
	}
	
	if(result==calculate){
		document.form.submit();
